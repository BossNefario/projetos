x = " Hello, WORLD! "
print(x.strip()) #remove os espaços antes e depois da setença
print(x.capitalize()) #coloca a sentença em caixa baixa
print(x.capitalize() and x.strip()) #coloca a sentença em caixa baixa e remove os espaços antes e depois da setença
print(x.upper()) #coloca a sentença em caixa alta
print(x.upper().strip()) #coloca a sentença em caixa alta e remove os espaços antes e depois da setença
print(x)